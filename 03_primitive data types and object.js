// Primitive Data Types:
// Primitive data types are a set of basic data types in javascript.

//There are the 7 Primitive Data types in javascript:
//>null, >number, >boolean, >bigint, >string, >symbol, >undefined

//You can learn these data types with a trick:
// nn bb ss u

// Example:
let a = null;
let b = 34;
let c = true; //also can be false\
let d = BigInt("567") + BigInt("3")
let e = "Suraj"
let f = Symbol("I am a nice icon")
let g = undefined
console.log(a, b, c, d, e, f, g)
console.log(typeof a, 
            typeof b, 
            typeof c, 
            typeof d, 
            typeof e, 
            typeof f, 
            typeof g)


